    // Carregar a tabela com chamados da lista
    public void carregarTabelaChamados(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"ID","Data","Tipo","Origem","Destino"},0);
        
        for (int i=0;i<listaChamados.size();i++){
            Object linha[] = new Object[]{listaChamados.get(i).getIdChamado(),
                                           listaChamados.get(i).getDataChamado(),
                                           listaChamados.get(i).getTipoChamado(),
                                           listaChamados.get(i).getOrigemChamado(),
                                           listaChamados.get(i).getDestinoChamado()};
            modelo.addRow(linha);
            
        }
        
        //Tabela recebe model
        tblChamados.setModel(modelo);
        
        tblChamados.getColumnModel().getColumn(0).setPreferredWidth(5);
        tblChamados.getColumnModel().getColumn(1).setPreferredWidth(5);
        tblChamados.getColumnModel().getColumn(2).setPreferredWidth(20);
        tblChamados.getColumnModel().getColumn(3).setPreferredWidth(55);
        tblChamados.getColumnModel().getColumn(4).setPreferredWidth(55);
    }    
    
    
