    private void bntOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntOkActionPerformed
        if (txtData.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo data deve ser informado", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        } else{
            Chamados datee;
            String data = "", destino = "",id = "",origem = "",tipo = "";
            int dat = Integer.parseInt(txtData.getText());
            
            
            for (int i=0; i <listaChamados.size(); i++){
                datee = listaChamados.get(i);
                
                if (dat == datee.getDataChamado()) {
                    data = String.valueOf(datee.getDataChamado());
                    destino = datee.getDestinoChamado();
                    id = String.valueOf(datee.getIdChamado());
                    origem = datee.getOrigemChamado();
                    tipo = datee.getTipoChamado();
                    
                }
        }
            if(data.equals("")) {
               JOptionPane.showMessageDialog(null, "Esta data não existe!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
                txtDestino.setText("");
                txtId.setText("");
                txtOrigem.setText("");
                txtTipo.setText("");
            } else {
                txtData.setText(data);
                txtDestino.setText(destino);
                txtId.setText(id);
                txtOrigem.setText(origem);
                txtTipo.setText(tipo);

            }
            txtData.selectAll();
            txtData.requestFocus(); 
        }
    }//GEN-LAST:event_bntOkActionPerformed

