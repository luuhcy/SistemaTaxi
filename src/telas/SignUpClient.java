    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
    
        if (txtId.getText().equals("") || 
                txtRg.getText().equals("") ||
                txtCpf.getText().equals("") ||
                txtNome.getText().equals("") ||
                txtTelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Todos os campos devem ser inseridos","Mensagem", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int id = Integer.parseInt(txtId.getText());
            String rg = txtRg.getText();
            long cpf = Long.parseLong(txtCpf.getText());
            String nome = txtNome.getText();
            int telefone = Integer.parseInt(txtTelefone.getText());
        
            if (botao.equals("novo")){
                Cliente cliente = new Cliente(id,rg,cpf,nome,telefone);
            
                //Adicionar cliente na lista
                listaCliente.add(cliente);
            
                JOptionPane.showMessageDialog(null,"Cliente cadastrado com sucesso","Mensagem", JOptionPane.INFORMATION_MESSAGE);
            } else if(botao.equals("editar")){
                int index = tblCliente.getSelectedRow();
                
                listaCliente.get(index).setId(id);
                listaCliente.get(index).setRg(rg);
                listaCliente.get(index).setCpf(cpf);
                listaCliente.get(index).setNome(nome);
                listaCliente.get(index).setTelefone(telefone);
            }
            
            //Carregar os dados do cliente na tabela
            carregarTabelaCliente();
            
            
            //Limpar os campos
            txtId.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtNome.setText("");
            txtTelefone.setText("");

            //Habilitar ou desabilitar bot√µes
            bntNovo.setEnabled(true);
            bntPesquisar.setEnabled(true);
            bntAlterar.setEnabled(false);
            bntExcluir.setEnabled(false);
            bntSalvar.setEnabled(false);
            bntCancelar.setEnabled(false);
            bntOk.setEnabled(false);

            //Habilitar ou desabilitar txt/cmb
            txtId.setEnabled(false);
            txtRg.setEnabled(false);
            txtCpf.setEnabled(false);
            txtNome.setEnabled(false);
            txtTelefone.setEnabled(false);
            cmbChamado.setEnabled(false);
        }
    }//GEN-LAST:event_bntSalvarActionPerformed

